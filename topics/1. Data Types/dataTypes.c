#include <stdio.h>

int main() {
    int age = 25;
    float pi = 3.14;
    char grade = 'A';
    double largeValue = 1.23456789;

    printf("Age: %d\n", age);
    printf("PI: %f\n", pi);
    printf("Grade: %c\n", grade);
    printf("Large Value: %lf\n", largeValue);

    return 0;
}

